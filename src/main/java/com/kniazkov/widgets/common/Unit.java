package com.kniazkov.widgets.common;

/**
 * Supported absolute CSS units.
 * <p>
 * These units correspond to standard CSS absolute length measurements.
 */
public enum Unit {

    /** Pixels. */
    PX,

    /** Points (1/72 of an inch). */
    PT,

    /** Picas (1 pica = 12 points). */
    PC,

    /** Inches. */
    IN,

    /** Centimeters. */
    CM,

    /** Millimeters. */
    MM;
}
